async function main() {
    console.log(`🚀 กำลังเริ่มนำข้อมูล FAQ เวอร์ชันสมบูรณ์เข้า Database...`);

    for (const faq of faqs) {
        await prisma.faq.upsert({
            where: { id: `faq-${faq.order}` },
            update: faq,
            create: {
                ...faq,
                id: `faq-${faq.order}`,
                status: 'active'
            },
        });
    }

    console.log(`✅ นำข้อมูลเข้าเรียบร้อยแล้วทั้งหมด ${faqs.length} ข้อ`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });

