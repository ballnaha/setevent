async function main() {
    console.log(`Start seeding FAQs...`);

    // Clear existing FAQs to avoid duplicates (Optional, but good for clean seed)
    try {
        await prisma.fAQ.deleteMany({});
        console.log(`Deleted existing FAQs.`);
    } catch (error) {
        console.log("Could not clear existing FAQs, possibly table doesn't exist yet or connection issue.");
    }


    for (const faq of faqs) {
        const createdFaq = await prisma.fAQ.create({
            data: faq,
        });
        console.log(`Created FAQ with id: ${createdFaq.id}`);
    }

    console.log(`Seeding finished.`);
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });


